#define _CRT_SECURE_NO_WARNINGS 1
#include<iostream>
#include<stdlib.h>

//编写代码，以给定值x为基准将链表分割成两部分，所有小于x的结点排在大于或等于x的结点之前
//给定一个链表的头指针 ListNode* pHead，
//请返回重新排列后的链表的头指针。注意：分割以后保持原来的数据顺序不变。

struct ListNode {
	int val;
	struct ListNode *next;
	ListNode(int x) : val(x), next(NULL) {}
};

class Partition {
public:
	ListNode* partition(ListNode* pHead, int x) {
		ListNode* LHead = (ListNode*)malloc(sizeof(ListNode));
		ListNode* HHead = (ListNode*)malloc(sizeof(ListNode));
		ListNode* L = LHead;
		ListNode* H = HHead;

		while (pHead)
		{
			if (pHead->val < x)
			{
				L->next = pHead;
				L = L->next;
				pHead = pHead->next;
			}
			else
			{
				H->next = pHead;
				H = H->next;
				pHead = pHead->next;
			}
		}
		H->next = NULL;

		H = HHead;
		L->next = HHead->next;
		L = LHead;
		LHead = LHead->next;
		free(L);
		free(H);

		return LHead;
	}
};


